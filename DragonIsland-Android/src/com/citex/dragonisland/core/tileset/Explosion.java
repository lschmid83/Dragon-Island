/**
 * This class represents a block explosion.
 * @author Lawrence Schmid
 */
public class Explosion {
	
	/** X coordinate. */
	public float x = 0;
	
	/** Y coordinate. */
	public float y = 0;
	
	/** Time elapse since explosion. */
	public int timer = 0;
	
	/** Direction of explosion. */
	public char direction = 'l';
};