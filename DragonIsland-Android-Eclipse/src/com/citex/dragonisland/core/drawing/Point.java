package com.citex.dragonisland.core.drawing;

/**
 * This class represents a point.
 * @author Lawrence Schmid.
 */
public class Point {
	
	/** X coordinate. */
	public float x;
	
	/** Y coordinate. */
	public float y;
	
	/**
	 * Initialises a Point object.
	 * @param x X coordinate.
	 * @param y Y coordinate.
	 */
	public Point(float x, float y) {
		this.x = x;
		this.y = y;
	}
}
